    public int NumSubarrayProductLessThanK(int[] nums, int k)
    {
        if (k <= 1) return 0;

        int count = 0;
        int leftIndex = 0;
        int multiply = 1;
        for (int rightIndex = 0; rightIndex < nums.Length; rightIndex++)
        {
            multiply *= nums[rightIndex];
            while (multiply >= k)
            {
                multiply /= nums[leftIndex];
                leftIndex++;
            }
            count += rightIndex - leftIndex + 1;
        }
        return count;
    }
