namespace Moons.Common20
{
    /// <summary>
    /// 控件数据类型
    /// </summary>
    public enum ControlDataType
    {
        /// <summary>
        /// 字符串
        /// </summary>
        String = 1,

        /// <summary>
        /// 日期
        /// </summary>
        DateTime = 2,

        /// <summary>
        /// 整型
        /// </summary>
        Int32 = 3,

        /// <summary>
        /// 长整型
        /// </summary>
        Int64 = 4,

        /// <summary>
        /// 双精度小数
        /// </summary>
        Double = 5,

        /// <summary>
        /// 浮点数
        /// </summary>
        Float = 6,
    }
}