        /// 未知
        /// </summary>
        Unknown,

        /// <summary>
        /// bool
        /// </summary>
        Boolean,

        /// <summary>
        /// byte
        /// </summary>
        Byte,

        /// <summary>
        /// short
        /// </summary>
        Int16,

        /// <summary>
        /// int
        /// </summary>
        Int32,

        /// <summary>
        /// long
        /// </summary>
        Int64,

        /// <summary>
        /// float
        /// </summary>
        Single,

        /// <summary>
        /// double
        /// </summary>
        Double,

        /// <summary>
        /// DateTime
        /// </summary>
        DateTime,

        /// <summary>
        /// string
        /// </summary>
        String,

        /// <summary>
        /// 可空单精度浮点数
        /// </summary>
        NullableSingle,

        /// <summary>
        /// 字节数组
        /// </summary>
        ByteArray,
    }
}